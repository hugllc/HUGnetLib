HUGnet.Progress = Backbone.View.extend({
    tag: 'div',
    initialize: function (options)
    {
        this.$el.dialog(options);
        this.render();
    },
    update: function (value)
    {
        if (value != false) {
            if (value < 1) {
                value = value * 100;
            }
            if (value >= 100) {
                this.remove();
            }
        }
        //if (this.$el.is(':data(dialog)')) {
            this.$bar.progressbar('option', 'value', value);
        //}
    },
    render: function ()
    {
        this.$el.html('<div id="progressbar"></div>');
        this.$bar = this.$('div#progressbar');
        this.$bar.progressbar({ value: 0 });
    }
});

