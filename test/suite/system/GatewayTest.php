    public function testCreate($config, $gateway, $expectCfg, $expectTable)
    {
        $obj = GatewayTestStub::create($config, $gateway);
        // Make sure we have the right object
        $this->assertTrue(is_subclass_of($obj, "HUGnet\Gateway"));
        // Make sure the configuration is set.
        $this->assertAttributeSame($expectCfg, "_config", $obj);
        if (is_array($expectTable)) {
            $table = &$this->readAttribute($obj, "_table");
            $this->assertTrue(is_object($table));
            $this->assertSame($expectTable, $table->toArray(false));
        } else {
            // Make sure the table is set
            $this->assertAttributeSame($expectTable, "_table", $obj);
        }
    }

