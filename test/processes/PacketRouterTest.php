                array(
                    "dummy" => true,
                ),
                array(
                    "dummy" => true,
                    "group" => "third",
                ),
            ),
        );
        $this->config = &ConfigContainer::singleton();
        $this->config->forceConfig($config);
        foreach($this->config->sockets->groups() as $group) {
            $this->socket[$group] = &$this->config->sockets->getSocket($group);
        }
        $this->o = new PacketRouter();
    }

