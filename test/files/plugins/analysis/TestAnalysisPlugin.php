class TestAnalysisPlugin extends DeviceProcessPluginBase
    implements DeviceProcessPluginInterface
{
    /** @var This is to register the class */
    public static $registerPlugin = array(
        "Name" => "testAnalysis",
        "Type" => "analysis",
        "Class" => "TestAnalysisPlugin",
    );
    /**
    * This function does the stuff in the class.
    *
    * @param DeviceContainer &$dev The device to check
    *
    * @return bool True if ready to return, false otherwise
    */
    public function main(DeviceContainer &$dev)
    {
        $dev->params->DriverInfo[__CLASS__]++;
        if (is_null($dev->params->DriverInfo["return"])) {
            return true;
        } else if (is_int($dev->params->DriverInfo["return"])) {
            $ret = $dev->params->DriverInfo["return"];
            $dev->params->DriverInfo["return"]++;
            return (bool)$ret;
        }
        return (bool)$dev->params->DriverInfo["return"];
    }
}