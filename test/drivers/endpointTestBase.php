    /**
     * Test
     *
     * @param array $val The array to print
     *
     * @return null
     */
    function printArrayVal($val) 
    {
        if (is_array($val)) {
            print "array(";
            $sep = "";
            foreach ($val as $key => $v) {
                if (is_array($v)) {
                    print "\n    ";
                    if (is_string($key)) print '"'.$key.'" => ';
                    endpointTestBase::printArrayVal($v);
                    print ",\n";
                } else {
                    if (is_string($key)) {
                        print '"'.$key.'" => ';
                    } else {
                        print $sep;
                        $sep = ", ";                    
                    }
                    endpointTestBase::printArrayVal($v);
                    if (is_string($key)) print ",\n";
                }
            }                
            print ")";
        } else if (is_string($val)) {
            print '"'.$val.'"';
        } else if (is_bool($val)) {
            if ($val) {
                print "true";
            } else {
                print "false";
            }
        } else if (is_null($val)) {
            print "null";
        } else {
            print $val;
        }
    }    
        
