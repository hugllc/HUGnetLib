    /**
    * Tests for verbosity
    *
    * @param array $preload The array to preload into the class
    * @param array $time    The time of the last packet
    * @param array $expect  The expected return
    *
    * @dataProvider dataToHistoryTable
    *
    * @return null
    */
    public function testToHistoryTable($db, $preload, $time, $prev, $expect)
    {
        foreach ($db as $p) {
            $this->o->clearData();
            $this->o->fromAny($p);
            $this->o->insertRow();
        }
        $this->o->clearData();
        $this->o->fromAny($preload);
        $array = $this->o->toHistoryTable($time, $prev)->toArray(false);
        $this->assertEquals($expect, $array, "", 0.1);
    }

