    public function testInsertRecord($preload, $expect)
    {
        $date = time();
        RawHistoryTable::insertRecord($preload);
        $stmt = $this->pdo->query("SELECT * FROM `rawHistory`");
        $rows = $stmt->fetchAll(PDO::FETCH_ASSOC);
